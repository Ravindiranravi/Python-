# Integer
mobileNo = 3445778894
print("integer Datatype",mobileNo,type(mobileNo))

temprature = 34.5
print("float Datatype",temprature,type(temprature))

complexNum = 34j+56
print("integer Datatype",complexNum,type(complexNum))

